import santalib as santa 

def test_aoc_examples_dump():
    examples = [
        ([1002,4,3,4,33], [1002,4,3,4,99]),
        ([1101,100,-1,4,0], [1101,100,-1,4,99])
        ]
    for before, after in examples:
        ic = santa.IntCode(before, [], lambda _: None)
        ic.run()
        assert ic.dump() == after
        

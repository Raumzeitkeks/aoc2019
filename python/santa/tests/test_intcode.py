from santa.intcode import IntCode

def test_dump():
    ex = [([1,9,10,3,2,3,11,0,99,30,40,50], [3500,9,10,70,2,3,11,0,99,30,40,50]),
          ([1,0,0,0,99],                    [2,0,0,0,99]                       ),
          ([2,3,0,3,99],                    [2,3,0,6,99]                       ),
          ([2,4,4,5,99,0],                  [2,4,4,5,99,9801]                  ),
          ([1,1,1,4,99,5,6,0,99],           [30,1,1,4,2,5,6,0,99]              ),
          ([1002,4,3,4,33],                 [1002,4,3,4,99]                    ),
          ([1101,100,-1,4,0],               [1101,100,-1,4,99]                 )]
    for before, after in ex:
        ic = IntCode(before)
        ic.run()
        assert ic.dump() == after


def test_output():
    ex = [([3,9,8,9,10,9,4,9,99,-1,8], [(-8,0), (7,0), (8,1), (9,0), (10,0)]),
          ([3,9,7,9,10,9,4,9,99,-1,8], [(-8,1), (7,1), (8,0), (9,0), (10,0)]),
          ([3,3,1108,-1,8,3,4,3,99],   [(-8,0), (7,0), (8,1), (9,0), (10,0)]),
          ([3,3,1107,-1,8,3,4,3,99],   [(-8,1), (7,1), (8,0), (9,0), (10,0)]),
          ([3,12,6,12,15,1,13,14,13,4,13,99,-1,0,1,9], [(-5,1), (-1,1), (0,0), (1,1), (5,1)]),
          ([3,3,1105,-1,9,1101,0,0,12,4,12,99,1],      [(-5,1), (-1,1), (0,0), (1,1), (5,1)]),
          ([3,21,1008,21,8,20,1005,20,22,107,8,21,20,1006,20,31,1106,0,36,98,0,0,1002,21,125,20,4,20,1105,1,46,104,999,1105,1,46,1101,1000,1,20,4,20,1105,1,46,98,99], [(-8,999), (7,999), (8,1000), (9,1001), (10,1001)]),]
    for code, in_out in ex:
        for input, exp_out in in_out:
            print("code:", code, "input:", input)
            output = []
            ic = IntCode(code, lambda: input, output.append)
            ic.run()
            print("dump:", ic.dump())
            assert output == [exp_out]
